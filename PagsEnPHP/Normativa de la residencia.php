		</ul>
		<h2>ASISTENCIA SANITARIA.</h2>
		<ul>
			<li>La asistencia sanitaria se realizará a través de la compañías de cada residente (Seguridad Social,
				Sanitas, etc.), siendo responsabilidad de la correspondiente compañía de asistencia sanitaria la
				atención domiciliaria, hospitalaria y el traslado del paciente a los centros de atención primaria u
				hospitalaria en caso necesario, debiendo recabar de ellas la información necesaria acerca de la
				ubicación de los Centros de atención y del servicio de asistencia a domicilio, en caso de que la
				compañía lo tenga.</li>
			<li>Los servicios de la Residencia Universitaria tratarán de facilitar al residente las gestiones necesarias
