				Universitaria están obligados a rellenar un impreso de salida, disponible en recepción, o en
				su caso, si esto fuera imposible, deberán comunicarlo por teléfono desde el lugar en que se
				encuentren.</li>
			<li>No se permite a los residentes la entrada en las zonas de cocina y máquinas.</li>
			<li>A partir de la medianoche se respetará el silencio escrupulosamente, en especial en
				habitaciones, pasillos y escaleras, evitando cualquier comportamiento molesto a los demás.</li>
			<li>Queda terminantemente prohibido el consumo o posesión de bebidas alcohólicas.</li>
			<li>La Dirección de la Residencia de estudiantes dará atento cumplimiento a las acciones judiciales
