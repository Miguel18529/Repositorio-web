			<li>Para ello, los interesados deberán comunicar a nuestro personal los datos necesarios para contactar
				con los correspondientes servicios médicos.</li>
			<li>El personal de la residencia no tiene la obligación de realizar el traslado sanitario del paciente, en
				casos puntuales solicitará autorización a Dirección y siempre sin menoscabo del cuidado del resto
