				enumeradas de la Lavandería, que se encuentra en el sótano del edificio.</li>
			<li>El horario de apertura de la Lavandería será a las 12:00 y a la 13:00. El horario puede cambiar de un
				curso a otro, por tanto se recomienda consultar en Recepción.</li>
		</ul>
		<h2>PREVENCIÓN DE INCENDIOS.</h2>
		<ul>
			<li>Si se detecta alguna anomalía o disfunción ponerla inmediatamente en conocimiento de la Recepción
				de la Residencia Universitaria.</li>
			<li>Las velas u objetos similares están totalmente prohibidos. Así como calentadores con resistencias.</li>
			<li>Al abandonar la habitación inspeccionadla, desconectad todos los aparatos y elementos eléctricos,
				incluidos los ordenadores personales, el aire acondicionado y las luces de la habitación antes de salir.
			</li>
			<li>No dejar ropa, tejidos o cualquier otro material inflamable cerca de enchufes o fuentes de calor.</li>
			<li>Mantener el orden dentro de la habitación.</li>
			<li>Si se observa que los elementos de extinción de incendios (extintores, mangueras, detectores…) o
				de señalización (señales o luces de emergencia) se encuentran en un estado no apto para su uso en
				caso de necesidad, notificarlo a Recepción lo antes posible.</li>
			<li>No hacer nunca uso de los medios de extinción, ni de la alarma si no es en caso de emergencia. No
				arrancar la señalización, todas serán necesarias en caso de emergencia</li>
			<li>No hacer fuego en las habitaciones, ni encender velas ni cualquier otro objeto que produzca llamas
			o calor intenso. No utilizar más aparatos eléctricos que los permitidos.</li>
			<li>No sobrecargar los enchufes.</li>
			<li>La mejor manera de protegernos del fuego es NO PROVOCARLO</li>
			<li>Está totalmente prohibido fumar en toda la Residencia estudiantil.</li>
		</ul>
		<h2>ACTUACIÓN EN CASO DE EMERGENCIA.</h2>
		<ul>
			<li>En cuanto se observe un riesgo potencial o real de incendio se deberá avisar a Recepción
				inmediatamente (extensión 9).</li>
			<li>Si hay un extintor cerca y cree poder apagar el fuego, puede utilizarlo. Si no se puede apagar el fuego
				abandónela zona inmediatamente.</li>
			<li>El uso indebido del extintor, se considera falta.</li>
			<li>Mantenga siempre la calma y no grite ni corra descontroladamente.</li>
			<li>Si el fuego prende la ropa, revuélquese sobre el suelo hasta apagarlo.</li>
			<li>Nunca abra una puerta sin tocarla primero con el dorso de la mano. Si la puerta está caliente, pruebe
				otra salida.</li>
			<li>En ningún caso se deberá retroceder a recoger objetos personales ni a buscar a otras personas.</li>
			<li>Camine con rapidez pero sin correr. No empuje en las escaleras, no olvide que se trata de un espacio
				seguro, espere a que la vía de evacuación quede libre.</li>
			<li>Utilice siempre la escalera de forma descendente.</li>
			<li>Si hay humo póngase, un pañuelo o cualquier tejido húmedo sobre la boca y la nariz.</li>
			<li>Evite siempre respirar el humo. Gatee si es necesario, el aire menos contaminado es el que está más
				cerca del suelo.</li>
			<li>Abandone el edifico y diríjase a la puerta principal junto al resto de la gente. Si una vez evacuado el
				edificio se detecta la ausencia de alguien que estaba dentro al iniciarse la emergencia, notifíquelo al
				equipo de evacuación.</li>
			<li>En caso de emergencia no pierda la calma. EL NERVIOSISMO y no el fuego es el peor enemigo.</li>
		</ul>
