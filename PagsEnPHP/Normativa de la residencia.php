			<li>Una vez terminado el curso, la Dirección de la Residencia Universitaria se reserva el derecho
				de renovar o no la plaza al residente, siendo los criterios que pueden determinar la no
				pertenencia en la Residencia Universitaria los siguientes: Falta de responsabilidad académica, cívica o
				social,
				falta de solidaridad con los/as compañeros/as, impuntualidad en el pago de las cuotas.</li>
		</ul>
