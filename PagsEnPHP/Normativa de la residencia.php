		<h2>MANTENIMIENTO DE HABITACIONES E INSTALACIONES.</h2>
		<ul>
			<li>La Residencia Universitaria dispone de personal encargado de solucionar y reparar las pequeñas
				averías que pudieran producirse tanto en las habitaciones como en cualquiera de las dependencias
				del edificio.</li>
			<li>Para informar de cualquier avería o desperfecto que se detecte, deberán dirigirse a la Gobernanta
				y cumplimentar el parte de avería, a fin de facilitar las tareas de mantenimiento y reparación. Tan
				pronto como pueda el personal de mantenimiento pasará por la habitación a solucionar la avería.</li>
			<li>Los residentes deberán tener las habitaciones disponibles, a fin de facilitar la actuación del personal
				de este servicio.</li>
			<li>A petición de la Directora, nuestro equipo de mantenimiento es el encargado a su vez de realizar
				todas las inspecciones periódicas de mantenimiento preventivo de todas las instalaciones del edificio.
			</li>
			<li>Por ello es posible que a lo largo del curso deban entrar en las habitaciones.</li>
		</ul>
		<h2>SERVICIO DE LAVANDERÍA.</h2>
		<ul>
			<li>Todas las prendas deberán estar marcadas de forma visible el número de habitación.</li>
			<li>El residente deberá aportar dos bolsas de redecillas, para la ropa interior blanca y para la ropa
				interior
				oscura. En este caso las bolsas irán marcadas de forma visible con el número de habitación.
				Para el resto de ropa, pueden aportar bolsas/cestas.</li>
			<li>Los residentes deberán colocar las bolsas de ropa para lavar, en el exterior de la puerta de su
				habitación el día que le haya sido asignado, en caso de quedar en el interior de la habitación el
				personal de servicio entenderá con esta señal, que no tienen ropa para lavar.</li>
			<li>La Dirección ni su personal no se responsabiliza de la selección realizada por el residente de la ropa
				a lavar.</li>
